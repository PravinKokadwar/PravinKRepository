package controlstatements;

public class MathDemo {

	public static void main(String[] args) 
	{
		int k=Math.abs(-4);
		
		//In Java, Math.abs function will return the absolute value of number.
		
		// Math.abs (x) = |x|= x if x>0
		//                   = 0 if x=0
        //                   = -x if x<0		
		
		System.out.println(k);
		
		int t=Math.addExact(34,78);
		
		System.out.println(t);
		
		}
}
